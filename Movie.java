
/**
 * Representation of a movie
 */
public class Movie {

	/**
	 * The name of the movie
	 */
	private String name;

	public Movie(String name) {
		this.name = name;
	}
	// getters and setters

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
