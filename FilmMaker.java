/**
 * Crews and Actors are Film makers
 */
public class FilmMaker {

	/*
	 * The name of the actor
	 */
	private String name;

	public FilmMaker(String name) {
		this.name = name;
	}

	// getters and setters
	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
